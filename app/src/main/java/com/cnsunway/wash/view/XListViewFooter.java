package com.cnsunway.wash.view;

import android.content.Context;
import android.util.AttributeSet;
import android.view.LayoutInflater;
import android.view.View;
import android.widget.ImageView;
import android.widget.LinearLayout;
import android.widget.TextView;

import com.cnsunway.wash.R;


public class XListViewFooter extends LinearLayout {
	public final static int STATE_NORMAL = 0;
	public final static int STATE_READY = 1;
	public final static int STATE_LOADING = 2;
	private Context mContext;
	private View mContentView;
	private View mProgressBar;
	private TextView mHintView;
	private TextView historyText;
	private ImageView foot_arrow;
	int state;
	public XListViewFooter(Context context) {
		super(context);
		initView(context);
	}
	
	public XListViewFooter(Context context, AttributeSet attrs) {
		super(context, attrs);
		initView(context);
	}
	
	public int getState(){
		return state;
	}

	
	public void setState(int state) {
		mHintView.setVisibility(View.INVISIBLE);
		mProgressBar.setVisibility(View.INVISIBLE);
		mHintView.setVisibility(View.INVISIBLE);
		this.state = state;
		if (state == STATE_READY) {
			mHintView.setVisibility(View.VISIBLE);
			mHintView.setText(R.string.xlistview_footer_hint_ready);
			
		} else if (state == STATE_LOADING) {
			
			
			mProgressBar.setVisibility(View.VISIBLE);
			
		} else {
			
			
			mHintView.setVisibility(View.VISIBLE);
			mHintView.setText(R.string.xlistview_footer_hint_normal);
		}
	}
	
	public void setBottomMargin(int height) {
		if (height < 0) return ;
		LayoutParams lp = (LayoutParams)mContentView.getLayoutParams();
		lp.bottomMargin = height;
		mContentView.setLayoutParams(lp);
	}
	
	public int getBottomMargin() {
		LayoutParams lp = (LayoutParams)mContentView.getLayoutParams();
		return lp.bottomMargin;
	}
	
	
	/**
	 * normal status
	 */
	public void normal() {
		mHintView.setVisibility(View.VISIBLE);
		mProgressBar.setVisibility(View.GONE);
		foot_arrow.setVisibility(View.GONE);
	}

	public void noMore(boolean prompt){
		if(prompt){
			mHintView.setVisibility(View.VISIBLE);
			mHintView.setText("- 没有更多了 -");
		}else{
			mHintView.setVisibility(View.GONE);
			hide();

		}

		mProgressBar.setVisibility(View.GONE);
//		foot_arrow.setVisibility(View.GONE);
	}
	
	
	/**
	 * loading status 
	 */
	public void loading() {
		mHintView.setVisibility(View.GONE);
		foot_arrow.setVisibility(View.GONE);
		mProgressBar.setVisibility(View.VISIBLE);
	}
	/**
	 * hide footer when disable pull load more
	 */
	public void hide() {
		LayoutParams lp = (LayoutParams)mContentView.getLayoutParams();
		lp.height = 0;
		mContentView.setLayoutParams(lp);
	}
	
	/**
	 * show footer
	 */
	public void show() {
		mContentView.setVisibility(View.VISIBLE);
		historyText.setVisibility(View.GONE);
		LayoutParams lp = (LayoutParams)mContentView.getLayoutParams();
		lp.height = LayoutParams.WRAP_CONTENT;
		mContentView.setLayoutParams(lp);
	}
	
	private void initView(Context context) {
		mContext = context;
		LinearLayout moreView = (LinearLayout)LayoutInflater.from(mContext).inflate(R.layout.xlistview_footer, null);
		addView(moreView);
		moreView.setLayoutParams(new LayoutParams(LayoutParams.FILL_PARENT, LayoutParams.WRAP_CONTENT));

		mContentView = moreView.findViewById(R.id.xlistview_footer_content);
		historyText = (TextView) moreView.findViewById(R.id.text_order_history);
		mProgressBar = moreView.findViewById(R.id.xlistview_footer_progressbar);
		mHintView = (TextView)moreView.findViewById(R.id.xlistview_footer_hint_textview);
//		foot_arrow= (ImageView)moreView.findViewById(R.id.foot_load_more_view_arrow);
//		
//		mRotateUpAnim = new RotateAnimation(0.0f, -180.0f,
//				Animation.RELATIVE_TO_SELF, 0.5f, Animation.RELATIVE_TO_SELF,
//				0.5f);
//		mRotateUpAnim.setDuration(ROTATE_ANIM_DURATION);
//		mRotateUpAnim.setFillAfter(true);
//		mRotateDownAnim = new RotateAnimation(-180.0f, 0.0f,
//				Animation.RELATIVE_TO_SELF, 0.5f, Animation.RELATIVE_TO_SELF,
//				0.5f);
//		mRotateDownAnim.setDuration(ROTATE_ANIM_DURATION);
//		mRotateDownAnim.setFillAfter(true);
		
		
	}

	public void showHistory(){
			mContentView.setVisibility(View.GONE);
		    historyText.setVisibility(View.VISIBLE);
	}

	public void showHistory(int text){
		mContentView.setVisibility(View.GONE);
		historyText.setVisibility(View.VISIBLE);
		historyText.setText(text);
	}

	public void hideHistory(){
		mContentView.setVisibility(View.VISIBLE);
		historyText.setVisibility(View.GONE);
	}



	
	
}
